int input[121];

int field_define()
{
    int i,j,l;
    char k;
    for(l = 1; l < 121; l = l + 3)
    {
        i = input[l];
        j = input[l+1];
        k = input[l+2];
        i = 2 * i + 2;
        j = 2 * i + 2;
        switch(k)
        {
            case 's': 
                i++;
                break;
            case 'e': 
                j++;
                break;
        }
        maze[i][j] = -1;
    }
    return 0;
}

